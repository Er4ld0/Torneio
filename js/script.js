function clickMenuTabela(){
    if (tabela.style.display == 'flex'){
        tabela.style.display = 'none'
    } else{
        tabela.style.display = 'flex'
    }

    if (menu__tabela.style.background == 'var(--perfil-background)'){
        menu__tabela.style.background = 'none'
    } else{
        menu__tabela.style.background = 'var(--perfil-background)'
    }

    if (menu__tabela.style.borderRadius == '15px'){
        menu__tabela.style.borderRadius = 'none'
    } else{
        menu__tabela.style.borderRadius = '15px'
    }


    if (games__main.style.display == 'none'){
        games__main.style.display = 'flex'
    } else{
        games__main.style.display = 'none'
    }
}

